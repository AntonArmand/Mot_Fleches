package fr.armand.models;

public class Cell {


    private int coordX;
    private int coordY;

    public void setCoordX(int parCoordX){
        coordX = parCoordX;
    }

    public void setCoordY(int parCoordY){
        coordY = parCoordY;
    }

    public int getCoordX(){
        return coordX;
    }

    public int getCoordY(){
        return coordY;
    }
}
